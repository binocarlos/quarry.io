		if(this.silent()){
			return;
		}
		children.push(child);
		addContainerToMap(child, map);

		listenerFunctions[child.quarryid()] = function(name, value){
			if(name.match(/^(class|tag|id)/)){
				removeContainerFromMap(child, map);
				addContainerToMap(child, map);
			}
		};

