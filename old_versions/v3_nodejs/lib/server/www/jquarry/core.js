		 			var searchRegistries = [];

		 			var addSearchRegistry = function(addSearchReg){
		 				
		 				searchRegistries.push(addSearchReg);
		 				
		 				_.each(addSearchReg.children(), function(child){
		 					addSearchRegistry(child.registry());
			 			})	
		 			}

